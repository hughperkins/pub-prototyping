#include <iostream>
#include <cinttypes>
using namespace std;

int main( int argc, char *argv[] ) {
    cout << "sizeof(int)" << sizeof(int) << endl;
    cout << "sizeof(long)" << sizeof(long) << endl;
    cout << "sizeof(long long)" << sizeof(long long) << endl;
    cout << "sizeof(int64_t)" << sizeof(int64_t) << endl;
    return 0;
}

